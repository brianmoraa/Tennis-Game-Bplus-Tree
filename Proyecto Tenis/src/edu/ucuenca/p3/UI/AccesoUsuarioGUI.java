    private void jButton_IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_IngresarActionPerformed
        try {
            String usuario = (String) jComboBox_Usuario.getSelectedItem();
            String clave = jPasswordField_Clave_Usuario.getText();
            ClaveSRV claveSrv = new ClaveSRV();

            if (usuario.equalsIgnoreCase("Jugador")) {
                Pantalla_Principal_Ranking.getInstancia().show();
                AccesoUsuarioGUI.getInstancia().hide();
            } else {
                if (claveSrv.ingresoUsuario(clave, usuario)) {
                    if (usuario.equalsIgnoreCase("Administrador")) {
                        Pantalla_Principal_Admin.getInstancia().show();
                        AccesoUsuarioGUI.getInstancia().hide();
                    } else {
                        Pantalla_Principal_Torneos.getInstancia().show();
                        AccesoUsuarioGUI.getInstancia().hide();

                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Acceso denegado !");
                }
            }
            jPasswordField_Clave_Usuario.setText("");
        } catch (NoSuchPaddingException ex) {
            Logger.getLogger(AccesoUsuarioGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeyException ex) {
            Logger.getLogger(AccesoUsuarioGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalBlockSizeException ex) {
            Logger.getLogger(AccesoUsuarioGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (BadPaddingException ex) {
            Logger.getLogger(AccesoUsuarioGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(AccesoUsuarioGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_IngresarActionPerformed

